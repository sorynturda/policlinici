package com.example.source.claseTabele;

public class OrarAngajat {
    private Integer zi;
    private String interval;

    public OrarAngajat(Integer zi, String interval) {
        this.zi = zi;
        this.interval = interval;
    }

    public Integer getZi() {
        return zi;
    }

    public String getInterval() {
        return interval;
    }
}
